    public void login(){
        boolean autenticado = false;
        String usuario, contrase単a;
        usuario = txt_user.getText();
        contrase単a = new String(txt_pass.getPassword());
        Usuario user = new Usuario(usuario, contrase単a);
        autenticado = UsuarioController.autenticar(user);
        if(!autenticado){
            JOptionPane.showMessageDialog(null, "Usuario y/o contrase単a incorrectos");
            return;
        }
        JOptionPane.showMessageDialog(null, "Bienvenido " + UsuarioController.nombre + " " + UsuarioController.apellido);
        
        if(UsuarioController.rol != 1 && UsuarioController.rol != 3  ){
            menu_alumno alumno = new menu_alumno();
            alumno.setVisible(true);
            this.dispose();
            return;
        }
        if(UsuarioController.rol != 2 && UsuarioController.rol != 3 ){
            menu_director director = new menu_director();
            director.setVisible(true);
            this.dispose();
            return;
        }
        menu_catedraticos catedraticos = new menu_catedraticos();
        catedraticos.setVisible(true);
        this.dispose();
        
    }
